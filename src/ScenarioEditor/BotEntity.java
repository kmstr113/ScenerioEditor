package ScenarioEditor;

public class BotEntity {
	private String id = "";
	private String name = "";
	
	
	public BotEntity() {
		
	}
	
	public void setID(String s) {
		id = s;
	}
	
	public String getID() {
		return id;
	}
	
	public void setName(String s) {
		name = s;
	}
	
	public String getName() {
		return name;
	}
}
